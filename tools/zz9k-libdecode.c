static int require_cap(uint32_t caps, uint32_t bit)
{
  const char *name;

  if ((caps & bit) != 0U) {
    return 1;
  }

  name = zz9k_capability_name(bit);
  printf("zz9k-libdecode: missing required capability: %s\n",
         name ? name : "unknown");
  return 0;
}

int main(void)
{
  ZZ9KCaps caps;
  ZZ9KSharedBuffer input;
  ZZ9KSurface surface;
  ZZ9KImageDecodeDesc desc;
  ZZ9KImageDecodeResult result;
  ZZ9KRect dst_rect;
  int input_allocated = 0;
  int surface_allocated = 0;
  int status;
  int rc = 1;
  uint32_t output_format;
  uint32_t output_pitch;
  uint32_t expected_output_bytes;

  memset(&input, 0, sizeof(input));
  memset(&surface, 0, sizeof(surface));

  printf("zz9k-libdecode: opening zz9k.library\n");
  ZZ9KBase = OpenLibrary((CONST_STRPTR)ZZ9K_LIBRARY_NAME,
                         ZZ9K_LIBRARY_VERSION);
  if (!ZZ9KBase) {
    printf("open failed\n");
    return 20;
  }

  printf("open ok base=0x%08lx version=%u revision=%u\n",
         (unsigned long)ZZ9KBase, ZZ9KBase->lib_Version,
         ZZ9KBase->lib_Revision);
  if (ZZ9KBase->lib_Revision <
      ZZ9K_LIBRARY_MIN_REVISION_TYPED_IMAGE_DECODE) {
    printf("zz9k.library revision too old for typed image decode LVOs\n");
    goto cleanup;
  }

  memset(&caps, 0, sizeof(caps));
  status = ZZ9KQueryCaps(&caps);
  if (status != ZZ9K_STATUS_OK) {
    printf("query caps failed: %s (%d)\n", zz9k_status_text(status),
           status);
    goto cleanup;
  }
  if (!require_cap(caps.capability_bits, ZZ9K_CAP_SHARED_ALLOC) ||
      !require_cap(caps.capability_bits, ZZ9K_CAP_SURFACES) ||
      !require_cap(caps.capability_bits, ZZ9K_CAP_IMAGE_DECODE)) {
    goto cleanup;
  }

  status = ZZ9KAllocShared((uint32_t)sizeof(jpeg_2x2), 4U, 0U, &input);
  if (status != ZZ9K_STATUS_OK) {
    printf("input alloc failed: %s (%d)\n", zz9k_status_text(status),
           status);
    goto cleanup;
  }
  input_allocated = 1;
  if (!zz9k_shared_copy_to(&input, 0U, jpeg_2x2,
                           (uint32_t)sizeof(jpeg_2x2))) {
    printf("input copy failed\n");
    goto cleanup;
  }

  output_format = zz9k_surface_native_rtg_format();
  if (!zz9k_surface_layout(2U, 2U, output_format, &output_pitch,
                           &expected_output_bytes)) {
    printf("unsupported native RTG output geometry\n");
    goto cleanup;
  }
  status = ZZ9KAllocSurface(2U, 2U, output_format,
                            ZZ9K_SURFACE_FLAG_ARM_LOCAL, &surface);
  if (status != ZZ9K_STATUS_OK) {
    printf("surface alloc failed: %s (%d)\n", zz9k_status_text(status),
           status);
    goto cleanup;
  }
  surface_allocated = 1;

  dst_rect.x = 0U;
  dst_rect.y = 0U;
  dst_rect.w = surface.width;
  dst_rect.h = surface.height;
  if (!zz9k_image_build_decode_desc(&desc, input.handle, 0U,
                                    (uint32_t)sizeof(jpeg_2x2),
                                    surface.handle, &dst_rect,
                                    output_format, 0U)) {
    printf("could not build typed JPEG decode descriptor\n");
    goto cleanup;
  }

  memset(&result, 0, sizeof(result));
  status = ZZ9KDecodeJpeg(&desc, &result);
  if (status != ZZ9K_STATUS_OK) {
    printf("typed JPEG decode failed: %s (%d)\n",
           zz9k_status_text(status), status);
    goto cleanup;
  }
  if (result.width != 2U || result.height != 2U ||
      result.output_format != output_format ||
      result.bytes_written != expected_output_bytes) {
    printf("unexpected result %lu x %lu format=%lu bytes=%lu\n",
           (unsigned long)result.width,
           (unsigned long)result.height,
           (unsigned long)result.output_format,
           (unsigned long)result.bytes_written);
    goto cleanup;
  }

  printf("typed JPEG decode ok (%lu x %lu %s, %lu bytes)\n",
         (unsigned long)result.width,
         (unsigned long)result.height,
         zz9k_surface_format_text(result.output_format),
         (unsigned long)result.bytes_written);
  rc = 0;

cleanup:
  if (surface_allocated) {
    ZZ9KFreeSurface(surface.handle);
  }
  if (input_allocated) {
    ZZ9KFreeShared(input.handle);
  }
  if (ZZ9KBase) {
    CloseLibrary(ZZ9KBase);
  }
  return rc;
}
